import mongoose from "mongoose";

// Schema definition for user data
const userSchema = new mongoose.Schema({
    name: {
        type: String,
        required: true,
    },
    email: {
        type: String,
        required: true,
        unique: true,
    },
    password: {
        type: String,
        required: true,
    },
});

// Create and export User model from schema
export default mongoose.model("User", userSchema);
